/**
 * 获取指定知识库的数据
 * @param {string} id - 知识库ID (格式: level_card_order, 如: N5_1)
 * @returns {Object|null} 知识库数据
 */
function getKnowledgeData(id) {
  return knowledgeData[id] || null;
}

/**
 * 获取所有知识库列表
 * @returns {Array} 知识库列表
 */
function getKnowledgeList() {
  return Object.keys(knowledgeData).map(id => ({
    id: id,
    title: knowledgeData[id].title,
    description: knowledgeData[id].description
  }));
}

module.exports = {
  knowledgeData,
  getKnowledgeData,
  getKnowledgeList
};